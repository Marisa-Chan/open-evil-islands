#include <iostream>

#include <boost/shared_ptr.hpp>

#include "mobfile.h"

int main(int argc, char* argv[])
{
	if (2 != argc) {
		std::cout << "Usage: " << argv[0] <<
			" <mob_name>" << std::endl;
		return 1;
	}

	boost::shared_ptr<mobfile> mob = boost::shared_ptr<mobfile>(
		mobfile_open_file(argv[1]), mobfile_close);
	if (!mob) {
		std::cout << "Could not open file '" << argv[1] << "'" << std::endl;
		return 1;
	}

	mobfile_debug_print(mob.get());

	return 0;
}
